        <p class="card-text">
            <strong> Qu’est-ce que la blockchain ?</strong>
              
            La blockchain est essentiellement un registre numérique décentralisé et transparent qui enregistre toutes
            les transactions de manière sécurisée. Au lieu d’être stockées dans une base de données centralisée, les
            informations sont réparties sur plusieurs ordinateurs, appelés nœuds, qui font partie du réseau blockchain.
            Chaque transaction est regroupée dans un bloc, qui est ensuite ajouté à une chaîne de blocs existante, d’où
            le terme « blockchain ».
        </p>
         
        <p class="card-text">
        <strong>Comment fonctionne la blockchain ?</strong>
                
            La blockchain fonctionne grâce à un processus appelé « consensus ». Lorsqu’une transaction est effectuée,
            elle est diffusée à l’ensemble du réseau. Les nœuds du réseau vérifient ensuite la validité de la
            transaction en utilisant des algorithmes complexes. Une fois qu’une majorité de nœuds confirment la validité
            de la transaction, elle est ajoutée à un nouveau bloc. Ce bloc est ensuite lié au bloc précédent, formant
            ainsi une chaîne de blocs sécurisée et immuable.
            </p>
        
            <p class="card-text">
            <strong>  Les caractéristiques clés de la blockchain</strong>
            
            Elle présente plusieurs caractéristiques clés qui en font une technologie unique et puissante :
            
            Décentralisation : elle est décentralisée, ce qui signifie qu’elle n’est pas contrôlée par une autorité
            centrale. Cela garantit la transparence, la résilience et l’absence de point de défaillance unique.
            Transparence et immutabilité : Toutes les transactions effectuées sur la chaine de blocks sont transparentes
            et visibles par tous les participants du réseau. De plus, une fois qu’une transaction est enregistrée dans
            un bloc, il est presque impossible de la modifier, assurant ainsi l’intégrité des données.
            Sécurité : elle utilise des algorithmes cryptographiques avancés pour garantir la sécurité des transactions.
            Chaque transaction est cryptée et liée à la précédente, formant une structure solide et inviolable.
            Confidentialité sélective : Bien que les transactions soient transparentes, l’identité des utilisateurs peut
            être protégée en utilisant des adresses cryptées. Cela permet une certaine confidentialité tout en
            maintenant la transparence globale du système.
            </p>
          
